package com.yeven.thread.framework.constant;

/**
 * 用于线程路由的任务执行模式。
 * 
 * <p><b>设计必要性与核心价值：</b></p>
 * <ul>
 *   <li><b>物理资源隔离规范：</b> 提供细粒度的线程池分类模型，强制约束开发者在声明任务时明确区分 CPU 计算密集型与 I/O 阻塞型任务。</li>
 *   <li><b>消除过载与饿死：</b> 保证大量的阻塞式外部调用（如外部 HTTP、DB 读取）不会耗尽 CPU 线程池的有效线程，同时也防止高占用的数学计算阻塞敏感请求，确保极高的并发稳定性。</li>
 * </ul>
 */
public enum ExecutionMode {
    /**
     * 用于阻塞型 I/O 操作（例如数据库访问、网络请求或远程服务调用）。
     * 
     * <p>此类任务通常涉及较长的等待时间，会被调度到专门的 I/O 线程池中运行。</p>
     */
    IO,
    /**
     * 用于 CPU 密集型计算任务（例如哈希计算、加解密或复杂的业务逻辑数据处理）。
     * 
     * <p>此类任务会一直占用 CPU 资源，会被调度到根据 CPU 核心数配置的 CPU 线程池中运行，避免过载。</p>
     */
    CPU,
    /**
     * 直接在当前调用线程同步运行，不进行额外的线程分发。
     * 
     * <p>适用于非常轻量级的操作，可以节省线程上下文切换的开销。</p>
     */
    DIRECT
}
